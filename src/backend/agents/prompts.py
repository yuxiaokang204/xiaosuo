import re

# ════════════════════════════════════════════════════════
# 内容安全过滤 — 清除生成文本中的元标签泄露
# ════════════════════════════════════════════════════════

_META_PATTERNS = [
    (r'\b伏笔\b', '线索'),
    (r'\b钩子\b', '悬念'),
    (r'\b楔子\b', '引子'),
    (r'\b本章高潮\b', '转折'),
    (r'（[^）]*写作要求[^）]*）', ''),
    (r'【[^】]*写作要求[^】]*】', ''),
    (r'\b契诃夫之枪\b', ''),
    (r'\b三句话定律\b', ''),
    (r'\b黄金三章\b', ''),
    (r'\b爽点\b', '精彩点'),
]

# AI味表达清洗模式
_AI_EXPRESSION_PATTERNS = [
    (r'眼中闪过一丝\S*', '目光微动'),
    (r'心中涌起一股\S*', '心头一紧'),
    (r'他不禁想到', '他想'),
    (r'终于明白', '明白'),
    (r'与此同时，', ''),
    (r'另一方面，', ''),
    (r'就在这时，', ''),
    (r'突然，', ''),
]


def sanitize_meta_text(text: str) -> str:
    """清除生成文本中泄露的元标签/写作指令。
    
    当LLM将prompt中的元标签（如"伏笔"）误写入正文时，
    用自然表达替换或删除这些元词。
    """
    for pattern, replacement in _META_PATTERNS:
        text = re.sub(pattern, replacement, text)
    # 清理多余空格
    text = re.sub(r'\n{3,}', '\n\n', text)
    return text.strip()


def sanitize_chapter_content(text: str) -> str:
    """章节内容专项清洗：清除所有元标签 + 修复AI味表达"""
    text = sanitize_meta_text(text)
    # 修复AI味表达
    for pattern, replacement in _AI_EXPRESSION_PATTERNS:
        text = re.sub(pattern, replacement, text)
    # 清理章节标题（如果LLM误输出）
    text = re.sub(r'第[一二三四五六七八九十百千]+章\s*', '', text)
    return text.strip()


