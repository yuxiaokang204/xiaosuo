def build_story_architect_user_prompt(
    title: str = "",
    theme: str = "",
    tone: str = "",
    chapter_count: int = 10,
    platform: str = "番茄",
    world_info: str = "",
    characters: str = "",
    start_chapter: int = 0,
    existing_outline: str = "",
    story_direction: str = "",
    depth_level: int = 1,
    title_analysis: dict = None,  # v6.1
) -> str:
    parts = []
    if title:
        parts.append(f"小说标题：《{title}》")

    # v6.1 标题核心要素注入
    if title_analysis and title_analysis.get("prompt_inject"):
        parts.append(f"\n【标题核心要素】\n{title_analysis['prompt_inject']}")

    parts.append(f"故事类型：{theme}")
    parts.append(f"风格基调：{tone}")
    parts.append(f"目标平台：{platform}")

    # 平台适配提示
    platform_tips = {
        "番茄": "快节奏、强冲突、每章必有转折，2000-3000字/章，1000字一个爽点",
        "飞卢": "极爽文、系统流、500字一个爽点，2000-2500字/章",
        "起点": "精品慢热、世界观深度、3000-5000字/章，2000字一个爽点",
        "七猫": "女频言情、情感细腻、2500-3500字/章",
        "书旗": "多元混合、类型适配"
    }
    if platform in platform_tips:
        parts.append(f"平台要求：{platform_tips[platform]}")

    if start_chapter > 0:
        parts.append(f"从第{start_chapter}章续写，共{chapter_count}章")
        if existing_outline:
            parts.append(f"前文大纲参考：{existing_outline}")
    else:
        parts.append(f"目标章节数：{chapter_count}章（必须精确）")
        if depth_level >= 1:
            parts.append("黄金三章要求：第一章前300字抛钩子，第二章给期待/反转，第三章给小回报")

    if story_direction:
        parts.append(f"\n【故事走向指引】\n{story_direction}")

    if world_info:
        parts.append(f"\n世界观框架：{world_info}")
    if characters:
        parts.append(f"\n核心角色：{characters}")

    if depth_level <= 0:
        parts.append("\n【SKELETON模式】仅输出3-5个核心结构要点，不需要完整JSON，不需要展开细节。")
    elif depth_level >= 2:
        parts.append("\n【POLISH模式】请审查已有大纲的因果链和节奏分布，标记问题章节并给出调整建议。")
    else:
        parts.append("\n请设计完整章节架构。**必须严格围绕标题核心要素展开**，确保大纲情节、章节标题、叙事弧与标题传达的核心意象一致。")
    return "\n".join(parts)


